package kz.dossier.dto;

public class TransportDTO {
    private String name;
    private String regNumber;
    private String mark;
    private String category;
    private String recieveDate;
    private String vin;
    private String publishYear;
    private String engineVolume;
    private String color;
    private String mass;
    private String allowedMaxMass;
    private String specialFeatures;
    private String reasonOfGettingOut;
    private String seriesAndRegNumber;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRegNumber() {
        return regNumber;
    }

    public void setRegNumber(String regNumber) {
        this.regNumber = regNumber;
    }

    public String getMark() {
        return mark;
    }

    public void setMark(String mark) {
        this.mark = mark;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getRecieveDate() {
        return recieveDate;
    }

    public void setRecieveDate(String recieveDate) {
        this.recieveDate = recieveDate;
    }

    public String getVin() {
        return vin;
    }

    public void setVin(String vin) {
        this.vin = vin;
    }

    public String getPublishYear() {
        return publishYear;
    }

    public void setPublishYear(String publishYear) {
        this.publishYear = publishYear;
    }

    public String getEngineVolume() {
        return engineVolume;
    }

    public void setEngineVolume(String engineVolume) {
        this.engineVolume = engineVolume;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getMass() {
        return mass;
    }

    public void setMass(String mass) {
        this.mass = mass;
    }

    public String getAllowedMaxMass() {
        return allowedMaxMass;
    }

    public void setAllowedMaxMass(String allowedMaxMass) {
        this.allowedMaxMass = allowedMaxMass;
    }

    public String getSpecialFeatures() {
        return specialFeatures;
    }

    public void setSpecialFeatures(String specialFeatures) {
        this.specialFeatures = specialFeatures;
    }

    public String getReasonOfGettingOut() {
        return reasonOfGettingOut;
    }

    public void setReasonOfGettingOut(String reasonOfGettingOut) {
        this.reasonOfGettingOut = reasonOfGettingOut;
    }

    public String getSeriesAndRegNumber() {
        return seriesAndRegNumber;
    }

    public void setSeriesAndRegNumber(String seriesAndRegNumber) {
        this.seriesAndRegNumber = seriesAndRegNumber;
    }
}
